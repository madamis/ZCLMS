    def format_area(self, area):
        strnum = str(area)
        left = strnum[:strnum.find('.')]
        right = strnum[strnum.find('.'):]

        keep = count = len(left)
        fin = ''
        for i in left:
            if count % 3 == 0 and count != 0 and count != keep:
                fin += ', '
            fin += i
            count -= 1
        right += ' Meters'
        fin += right
        return fin

